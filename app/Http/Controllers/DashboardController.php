     $arr =[];
     $jumlah=[];
     foreach ($data as $value){
        $arr[] = $value->tanggal_peminjaman;
        $jumlah[] = $value->jumlah;
     }
     
     return view('pages.admin.dashboard',[
        'tanggal' => $arr,
        'jumlah' => $jumlah,
     ]);   
    }

